public class GUGUDAN {
    public static void main(String[] args){
        for(int j=1;j<10;j++){
            int x=j;
            for(int i=1;i<10;i++){
                int y=i*x;
                System.out.printf(x+"X"+i+"="+y+"\n");
            }
            System.out.println();;
        }
    }
}
